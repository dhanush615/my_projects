# Without R Objects:
print(5 + 3) # Addition
print(9 * 2) # Multiplication
print(10 / 2) # Division
print(15 - 4) # Subtraction

# With R Objects:
x <- 10 # Assign value to x
y <- 5  # Assign value to y
add <- x + y # Addition using objects
sub <- x - y # Subtraction using objects
mul <- x * y # Multiplication using objects
div <- x / y # Division using objects
paste("RESULTS: add, sub, mul, div", add, sub, mul, div)


